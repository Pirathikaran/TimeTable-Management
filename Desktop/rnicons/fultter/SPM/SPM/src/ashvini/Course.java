    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
          if(!textbox_search.getText().isEmpty()){
                Object sel1 = comboselect.getSelectedItem();
                String selitem = sel1.toString();
            
            if("Subject Name".equals(selitem)){
                try{
                    String sql2 = "SELECT * FROM Course WHERE name = '"+textbox_search.getText()+"'";
                    rs = st.executeQuery(sql2);
                    search_table.setModel(DbUtils.resultSetToTableModel(rs));
                        if(search_table.getModel().getRowCount()<=0)
                            JOptionPane.showMessageDialog(null,"The Name you have entered is NOT available");
                    }
                 catch(HeadlessException | SQLException ex){
                    JOptionPane.showMessageDialog(null, ex);
                 }
            }
            else if("Subject Code".equals(selitem)){
                try{
                    String sql2 = "SELECT * FROM Course WHERE code = '"+textbox_search.getText()+"'";
                    rs = st.executeQuery(sql2);
                    search_table.setModel(DbUtils.resultSetToTableModel(rs));
                        if(search_table.getModel().getRowCount()<=0)
                            JOptionPane.showMessageDialog(null,"The Code you have entered is NOT available");
                    }
                 catch(HeadlessException | SQLException ex){
                    JOptionPane.showMessageDialog(null, ex);
                 }
            }
            else if("Offered Year".equals(selitem)){
                try{
                    String sql2 = "SELECT * FROM Course WHERE year = '"+textbox_search.getText()+"'";
                    rs = st.executeQuery(sql2);
                    search_table.setModel(DbUtils.resultSetToTableModel(rs));
                        if(search_table.getModel().getRowCount()<=0)
                            JOptionPane.showMessageDialog(null,"No courses available in this year");
                    }
                 catch(HeadlessException | SQLException ex){
                    JOptionPane.showMessageDialog(null, ex);
                 }
            }
         }
         else
             JOptionPane.showMessageDialog(null,"PLEASE SPECIFY WHAT YOU NEED TO SEARCH");
        
    }//GEN-LAST:event_jButton2ActionPerformed

