    private void viewbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewbtnActionPerformed
        // TODO add your handling code here
        if(!tableUpdate.getSelectionModel().isSelectionEmpty()){

            try{
                int i = tableUpdate.getSelectedRow();
                TableModel model = tableUpdate.getModel();

                detailsTextArea.setText("                                  Location DETAILS\n\n\n");
                detailsTextArea.setText(detailsTextArea.getText()+"Building Name : " + model.getValueAt(i, 1).toString()+"\n");
                detailsTextArea.setText(detailsTextArea.getText()+"Room No : " + model.getValueAt(i, 2).toString()+"\n");
                detailsTextArea.setText(detailsTextArea.getText()+"Room Capacity : " + model.getValueAt(i, 3).toString()+"\n");
                detailsTextArea.setText(detailsTextArea.getText()+"Room type: " + model.getValueAt(i, 4).toString()+"\n");

            }catch(Exception ex){

            }
        }
        else
        JOptionPane.showMessageDialog(null, "NOTHING SPECIFIED TO VIEW");

    }//GEN-LAST:event_viewbtnActionPerformed

