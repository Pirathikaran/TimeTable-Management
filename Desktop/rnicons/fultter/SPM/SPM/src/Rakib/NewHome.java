    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:

        String search=txtSearch.getText();

        try{

            String sql="select * from record where building='"+search+"'";

            rs=st.executeQuery(sql);

            if(rs.next()){
                String add1=rs.getString("building");
                txtBuilding.setText(add1);
                String add2=rs.getString("room");

                txtRoom.setText(add2);
                String add3=rs.getString("capacity");
                txtCapacity.setText(add3);

            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }

    }//GEN-LAST:event_txtSearchKeyReleased

